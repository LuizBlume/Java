public class variaveisBooleanas {
    public static void main(String[] args) {
        double nota1 = 9;
        double nota2 = 8;
        
        if (nota1 > nota2) {
            System.out.println("A nota 1 é maior");
        }
        else {
            System.out.println("A nota 2 é maior");
        }
    }
}
