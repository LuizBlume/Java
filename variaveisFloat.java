public class variaveisFloat {
    public static void main(String[] args) {
        double nota1 = 9.5; // float
        double nota2 = 8; // float
        
        System.out.println("Minha primeira nota foi: " + nota1);
        System.out.println("Minha segunda nota foi: " + nota2);

        double mediaNota = (nota1 + nota2) / 2;
        System.out.println("A média das notas foram: " + mediaNota);
    }
}
